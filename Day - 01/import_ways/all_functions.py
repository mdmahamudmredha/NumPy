# Module
def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero"

def average(a, b):
    return (a + b) / 2

def square(a):
    return a * a

def cube(a):
    return a * a * a

def modulus(a, b):
    return a % b

def power(a, b):
    return a ** b

def floor_division(a, b):
    return a // b
